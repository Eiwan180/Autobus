    public Usuario(Operation operacion) {
        this.operacion = operacion;
        setTitle("Venta de Boleto");
        setLayout(new FlowLayout());
        
        JLabel nombreLabel = new JLabel("Nombre:");
        txtNombre = new JTextField(20);
        
        JLabel destinoLabel  = new JLabel("Destino:");
        txtDestino = new JTextField(20);
        
        JLabel precioLabel  = new JLabel("Precio:");
        txtPrecio = new JTextField(10);
        
        // Asientos disponibles
        JLabel asientoLabel   = new JLabel("Selecciona Asiento:");
        cmbCombo = new JComboBox<>();
        actualizarAsientosDisponibles();

        JButton venderBoletoButton = new JButton("Vender Boleto");
        venderBoletoButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                venderBoleto();
            }
        });
        add(nombreLabel);
        add(txtNombre);
        add(destinoLabel);
        add(txtDestino);
        add(precioLabel);
        add(txtPrecio);
        add(asientoLabel);
        add(cmbCombo);
        add(venderBoletoButton);

        setSize(300, 250);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setLocationRelativeTo(null);
    }
